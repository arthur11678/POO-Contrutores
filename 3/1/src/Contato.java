

public class Contato {
    int numero;
    int ddd;
    String operadora;
    
    public void set_numero(int numero){
        this.numero = numero;
    }
    public int get_numero(){
        return this.numero;
    }
    public void set_ddd(int ddd){
        this.ddd = ddd;
    }
    public int get_ddd(){
        return this.ddd;
    }
    public void set_operadora(String operadora){
        this.operadora = operadora;
    }
    public String get_operadora(){
        return this.operadora;
    }
}
