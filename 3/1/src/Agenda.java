

public class Agenda {
    String nome;
    String cidade;
    String deOndeEuConheço; 



    public String get_nome(){
        return this.nome;
    }
    public void set_nome(String nome){
        this.nome = nome;
    }
    public String get_cidade(){
        return this.cidade;
    }
    public void set_cidade(String cidade){
        this.cidade = cidade;
    }
    public String get_deOndeEuConheço(){
        return this.deOndeEuConheço;
    }
    public void set_deOndeEuConheço(String deOndeEuConheço){
        this.deOndeEuConheço = deOndeEuConheço;
    }
}
